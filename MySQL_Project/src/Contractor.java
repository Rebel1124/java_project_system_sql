/**
*
* The Contractor class stores the attributes for the contractors.
* 
*/
public class Contractor {
	
	// Attributes
	String name;
	String telNumber;
	String email;
	String address;
		
	// Methods (Constructor)
	public Contractor(String name, String telNumber, String email, String address) {
	  this.name = name;
	  this.telNumber = telNumber;
	  this.email = email;
	  this.address = address;
	}

}
